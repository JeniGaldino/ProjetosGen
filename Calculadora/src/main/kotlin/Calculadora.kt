class Calculadora{

    companion object{


        fun add(n1: Double,n2:Double): Double {
        val result = n1+n2
            return result

        }

        fun sub(n1: Double,n2:Double): Double {
            val result = n1-n2
            return result

        }
        fun multi(n1: Double,n2:Double): Double {
            val result = n1*n2
            return result
        }
        fun div(n1: Double,n2:Double): Double {
            val result = n1/n2
            return result
        }
    }



}